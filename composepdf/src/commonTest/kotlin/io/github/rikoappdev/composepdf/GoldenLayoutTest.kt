/**
 * Cross-platform identity gate. Runs the layout engine over a fixed, multi-feature document using a
 * deterministic stub font (no font files needed), then asserts the exact glyph origins (page, x, y,
 * size, weight, glyph ids) against a checked-in golden.
 *
 * Because the engine is integer-only shared Kotlin, this golden must reproduce **byte-for-byte on
 * JVM, Android and iOS**. Running it on each platform's test task is the empirical proof that output
 * is "identical to the dot" everywhere; it also guards against a regression introducing a float or a
 * platform-specific call.
 */
class GoldenLayoutTest {

    @Test
    fun layoutIsIdenticalToGolden() {
        assertEquals(GOLDEN, goldenCanonical())
    }
}

/** Monospace-ish deterministic metrics in integer font units (unitsPerEm = 1000). */
private object StubMetrics : TextMetrics {
    private const val UPM = 1000L
    private const val ADVANCE = 600L // every glyph is 0.6 em wide
    private const val ASCENT = 800L

    override fun shape(text: String, w: FontWeight): IntArray = IntArray(text.length) { text[it].code }
    override fun measureWidthPt(text: String, w: FontWeight, fontSizePt: Int): Int =
        ((text.length * ADVANCE * fontSizePt + UPM / 2) / UPM).toInt()
    override fun widthOfPt(gids: IntArray, w: FontWeight, fontSizePt: Int): Int =
        ((gids.size * ADVANCE * fontSizePt + UPM / 2) / UPM).toInt()
    override fun ascentPt(w: FontWeight, fontSizePt: Int): Int =
        ((ASCENT * fontSizePt + UPM / 2) / UPM).toInt()
}

/** A fixed document exercising wrapping, alignment, weighted rows, a bordered box and a paginating table. */
private fun goldenDocument(): PdfDocumentSpec = pdfDocument(PageConfig(size = PageSize.A4, margin = 36.dp)) {
    header {
        row {
            cell(2f) { text("ACME Inc.", TextStyle(fontSize = 10.sp, fontWeight = FontWeight.Bold)) }
            cell(1f) { text("Report", TextStyle(fontSize = 8.sp, align = TextAlign.End)) }
        }
        divider()
    }
    footer { text("Generated by compose-pdf", TextStyle(fontSize = 8.sp, color = Color(0xFF7F807F))) }

    text("Quarterly Summary", TextStyle(fontSize = 18.sp, fontWeight = FontWeight.Bold))
    spacer(8.dp)
    text(
        "This paragraph is long enough to wrap across several lines so the greedy line breaker " +
            "and the alignment math are exercised deterministically on every platform.",
        TextStyle(fontSize = 10.sp),
    )
    spacer(6.dp)
    row(gap = 8.dp) {
        cell(1f) { text("Left", TextStyle(fontSize = 9.sp)) }
        cell(1f) { text("Center", TextStyle(fontSize = 9.sp, align = TextAlign.Center)) }
        cell(1f) { text("Right", TextStyle(fontSize = 9.sp, align = TextAlign.End)) }
    }
    spacer(6.dp)
    box(padding = 8.dp, border = 1.dp) {
        text("Boxed note", TextStyle(fontSize = 9.sp, fontWeight = FontWeight.Bold))
    }
    spacer(6.dp)
    table(
        columns = listOf(
            PdfColumn(3f, "Item"),
            PdfColumn(1f, "Qty", TextAlign.End),
            PdfColumn(1f, "Price", TextAlign.End),
        ),
    ) {
        for (i in 1..40) row("Item number $i", "$i", "${i * 7}.00")
        totalRow("Total", "", "1234.00")
    }
}

/** Canonical, stable string of every glyph origin: `page|x,baselineY|fontSize|weightOrdinal|gids`. */
internal fun goldenCanonical(): String {
    val pages = layout(goldenDocument(), StubMetrics)
    val sb = StringBuilder()
    sb.append("pages=").append(pages.size).append('\n')
    for ((p, page) in pages.withIndex()) {
        for (op in page.ops) if (op is TextOp) {
            sb.append(p).append('|')
                .append(op.xPt).append(',').append(op.baselineYPt).append('|')
                .append(op.fontSizePt).append('|')
                .append(op.weight.ordinal).append('|')
                .append(op.gids.joinToString(","))
                .append('\n')
        }
    }
    return sb.toString()
}

